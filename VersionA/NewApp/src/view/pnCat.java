    private void setForm() {
        int row = tbMain.getSelectedRow();
        String id = tbMain.getValueAt(row, 0).toString();
        String name = tbMain.getValueAt(row, 1).toString();
        tfID.setText(id);
        tfName.setText(name);
    }
    
    private boolean isValid(Category objCat,String function) {
        boolean result = true;
        switch(function){
            case "add":
                if(objCat.getName().isEmpty()){
                    JOptionPane.showConfirmDialog(new pnCat(), "Chưa nhập tên","Thông báo",JOptionPane.WARNING_MESSAGE);
                    result = false;
                }
                break;
            case "edit":
                if(objCat.getId()<0){
                    JOptionPane.showConfirmDialog(new pnCat(), "Chưa chọn dòng cập nhật","Thông báo",JOptionPane.WARNING_MESSAGE);
                    result = false;
                }
                if(objCat.getName().isEmpty()){
                    JOptionPane.showConfirmDialog(new pnCat(), "Chưa nhập tên","Thông báo",JOptionPane.WARNING_MESSAGE);
                    result = false;
                }
                break;
            case "del":
                if(objCat.getId()<0){
                    JOptionPane.showConfirmDialog(new pnCat(), "Chưa chọn dòng để xóa","Thông báo",JOptionPane.WARNING_MESSAGE);
                    result = false;
                }
                break;
        }
        return result;
    }

