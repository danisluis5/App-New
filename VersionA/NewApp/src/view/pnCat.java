    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int row = tbMain.getSelectedRow();
        int id_cat = Integer.parseInt(tfID.getText());
        int result = 0;
        String name = tfName.getText();
        if(!new ValidateDB().category_exitsname(name, id_cat)){
            Category objCat = new Category(id_cat, name);
            if(isValid(objCat, "edit")){
                result = controller.editItem(objCat,row);
                if(result > 0){
                    JOptionPane.showConfirmDialog(new pnCat(), "Cập nhật thành công","Thông báo",JOptionPane.WARNING_MESSAGE);
                }else{
                    JOptionPane.showConfirmDialog(new pnCat(), "Cập nhật thất bại","Thông báo",JOptionPane.WARNING_MESSAGE);
                }
            }
        }else{
            JOptionPane.showConfirmDialog(new pnCat(), "Không trùng với tên danh mục hiện có","Thông báo",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btSuaActionPerformed

    private void btTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTimKiemActionPerformed
        // TODO add your handling code here:
        TableRowSorter sorter = new TableRowSorter(tbMain.getModel());
        tbMain.setRowSorter(sorter);
        
        String id = tfIDF.getText();
        RowFilter<AbstractTableModel,Object> filterID = RowFilter.regexFilter(id, 0);
        
        sorter.setRowFilter(filterID);
        
        String name = tfNameF.getText();
        RowFilter<AbstractTableModel,Object> filterName = RowFilter.regexFilter(name, 1);
        
        ArrayList<RowFilter<AbstractTableModel,Object>> alist = new ArrayList<>();
        alist.add(filterID);
        alist.add(filterName);
        
        RowFilter<AbstractTableModel,Object> filterAnd = RowFilter.andFilter(alist);
        sorter.setRowFilter(filterAnd);
    }//GEN-LAST:event_btTimKiemActionPerformed

