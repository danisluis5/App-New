    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        String name = tfName.getText();
        if(!new ValidateDB().category_exits(name)){
            Category objCat = new Category(0,name);
            if(isValid(objCat,"add")){
                int result = controller.addItem(objCat);
                if(result > 0){
                    JOptionPane.showMessageDialog(new pnCat(), "Thêm thành công","Thông báo",JOptionPane.WARNING_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(new pnCat(), "Có lỗi trong quá trình thêm","Thông báo",JOptionPane.WARNING_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(new pnCat(), "Xin lỗi bạn đã tồn tại trong hệ thống","Thông báo",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        int row = tbMain.getSelectedRow();
        int id_cat = 0;
        if(tfID.getText().isEmpty()){
            id_cat = 0;
        }else{
            id_cat = Integer.parseInt(tfID.getText());
        }
        String name = "";
        Category objCat = new Category(id_cat,name);
        if(isValid(objCat, "del")){
            int result = controller.deleteItem(objCat,row);
            if(result > 0){
                this.resetForm();
                JOptionPane.showConfirmDialog(new pnCat(), "Xóa thành công","Thông báo",JOptionPane.WARNING_MESSAGE);
            }else{
                JOptionPane.showConfirmDialog(new pnCat(), "Xóa thất bại","Thông báo",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

